package com.company;

// Create a Calculator Class to perform calculations on int and double data types (uses method overloading)
public class Calculator {

    // Addition, Subtraction, Multiplication, and Division methods for two int types
    public int add(int a, int b) {
        return a + b;
    }

    public int subtract(int a, int b) {
        return a - b;
    }

    public int multiply(int a, int b) {
        return a * b;
    }

    public int divide(int a, int b) {
        return a / b;
    }

    // Addition, Subtraction, Multiplication, and Division methods for two double types
    public double add(double a, double b) {
        return a + b;
    }

    public double subtract(double a, double b) {
        return a - b;
    }

    public double multiply(double a, double b) {
        return a * b;
    }

    public double divide(double a, double b) {
        return a / b;
    }

}
